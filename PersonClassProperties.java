public class Person {
  /**
    *
    * Within the class, at the top is where you define you class properties.
    * Properties represent objects that can be used inside and/or outside of the class.
    *
    * Properties declared as `public` are accesable from both inside & outside the class.
    * Properties declared as `private` are accessable from inside the class ONLY.
    *
    * In the Person class context, public properties would be firstName & age ( see below ).
    * private properties, for this example would be closer to a secret, of sorts.
    *
    * when defining class properties, try to think about where the value will be used.
    *
    **/
    
    public String firstName;
    public String age;
    
    private String deepestDarkestSecret;
    
}
